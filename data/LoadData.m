function [data, chars] = LoadData
    
    chars_to_load = {'a', ...
                     'B', ...
                     'e', ...
                     'l', ...
                     'M', ...
                     'o', ...
                     'P', ...
                     'plus', ...
                     's', ...
                     'T', ...
                     'Z', ...
                     };
    
    data = {};
    chars = {};
    for i = 1:size(chars_to_load, 2)
        filename = ['data_', chars_to_load{i}, '.mat'];
        data_i = load(filename);
        data  = [data, data_i.data];
        chars = [chars, repmat({chars_to_load{i}}, 1, size(data_i.data, 2))];
    end
    
end