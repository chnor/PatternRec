function [data, chars] = LoadData
    
    chars_to_load = {'a', ...
                     'b', ...
                     'c', ...
                     'd', ...
                     'e', ...
                     'f', ...
                     'g', ...
                     'h', ...
                     'i', ...
                     'j', ...
                     'k', ...
                     'l', ...
                     'o', ...
                     'p', ...
                     'plus', ...
                     'q', ...
                     'r', ...
                     's', ...
                     't', ...
                     'u', ...
                     'v', ...
                     'B', ...
                     'M', ...
                     'P', ...
                     'T', ...
                     'Z', ...
                     };
    
    data = {};
    chars = {};
    for i = 1:size(chars_to_load, 2)
        if strcmp(lower(chars_to_load{i}), chars_to_load{i})
            filename = ['data_', chars_to_load{i}, '.mat'];
        else
            % Upper case letter - handle specially
            filename = ['data_', lower(chars_to_load{i}), '_upper.mat'];
        end
        data_i = load(filename);
        data  = [data, data_i.data];
        chars = [chars, repmat({chars_to_load{i}}, 1, size(data_i.data, 2))];
    end
    
end